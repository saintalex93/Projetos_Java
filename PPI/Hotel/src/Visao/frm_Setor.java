 public void preencherTabela(String Sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{
            "<html><span style='color:#4b92db;font-weight: bold;'>CÃ³digo Setor</span></html>",
            "<html><span style='color:#4b92db;font-weight: bold;'>Setor</span></html>",

        };

        NumberFormat z = NumberFormat.getCurrencyInstance();
        conecta.executaSql(Sql);
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("CodSetor"), conecta.rs.getString("NomeSetor")});
            } while (conecta.rs.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao preencher a Tabela" + e);

        }

        ModeloTabelas modelo = new ModeloTabelas(dados, Colunas); //Instacia a classe do modelo da Tabela.
        JtableSetor.setModel(modelo);
        JtableSetor.getColumnModel().getColumn(0).setPreferredWidth(161); // Tamanho em pixel da coluna
        JtableSetor.getColumnModel().getColumn(0).setResizable(false);
        JtableSetor.getColumnModel().getColumn(1).setPreferredWidth(162);
        JtableSetor.getColumnModel().getColumn(1).setResizable(false);
  
        JtableSetor.setAutoResizeMode(JtableSetor.AUTO_RESIZE_OFF);//NÃ£o pode ser redimensionada
        JtableSetor.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }
    
