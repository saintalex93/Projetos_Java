    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (botao == 0)
        {

        if(preencheObjeto())
        {
            try {
                controladorSetor.Incluir(objSetor);
                preencherTabela("Select * From Setor");
                limparcampos();
            } catch (Exception ex) {
                Logger.getLogger(frm_Setor.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
        }else
        {
         if(preencheObjetoAltera())
        {
            try {
                controladorSetor.Alterar(objSetor);
                preencherTabela("Select * From Setor where CodSetor = '"+Codigo+"'");
                limparcampos();
                botao = 1;
                btnSair.setText("Cancelar");
            } catch (Exception ex) {
                Logger.getLogger(frm_Setor.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
        
        }
          

             // TODO add your handling code here:
    }//GEN-LAST:event_btnCadastrarActionPerformed

