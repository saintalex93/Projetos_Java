    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (Botoes == 0) {

            if (preencheObjeto()) {
                try {
                    DAO.Incluir(ObjLancaCategoria);

                    preencherTabela("SELECT * FROM Categoria");
                    limpadados();
                    //LimparCampos();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao Lançar dados: " + ex);
                }

            }
        } else {
            System.out.println(CodCategoria);

            if (preencheObjetoAltera()) {
                try {
                    DAO.Alterar(ObjAlteraCategoria);

                    preencherTabela("SELECT * FROM Categoria where CodCategoria = '"+CodCategoria+"'");
                    limpadados();
                    //LimparCampos();

                    Botoes = 0;
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao alterar dados: " + ex);
                }
                
            }

        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

