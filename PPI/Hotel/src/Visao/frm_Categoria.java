    public boolean preencheObjeto() {

        ObjLancaCategoria.setCategoriaApartamento(txt_categoria.getText());
        ObjLancaCategoria.setMaxPax(Integer.parseInt(txtMaxPess.getText()));
        ObjLancaCategoria.setValorDiaria(Double.parseDouble(txt_ValorDiaria.getText().replace(".", "").replace(",", ".")));

        return true;
    }

    public boolean preencheObjetoAltera() {

        ObjAlteraCategoria.setCodCategoria(CodCategoria);
        ObjAlteraCategoria.setCategoriaApartamento(txt_categoria.getText());
        ObjAlteraCategoria.setMaxPax(Integer.parseInt(txtMaxPess.getText()));
        ObjAlteraCategoria.setValorDiaria(Double.parseDouble(txt_ValorDiaria.getText().replace(".", "").replace(",", ".")));

        return true;
    }

    public void preencherTabela(String Sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{
            "<html><span style='color:#4b92db;font-weight: bold;'>Cód Categoria</span></html>",
            "<html><span style='color:#4b92db;font-weight: bold;'>Categoria</span></html>",
            "<html><span style='color:#4b92db;font-weight: bold;'>Máx Hóspedes</span></html>",
            "<html><span style='color:#4b92db;font-weight: bold;'>Valor Diária</span></html>"
        };

        NumberFormat z = NumberFormat.getCurrencyInstance();
        conecta.executaSql(Sql);
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("CodCategoria"), conecta.rs.getString("CategoriaApto"), conecta.rs.getInt("MaxPax"), z.format(conecta.rs.getDouble("ValorDiaria"))});
            } while (conecta.rs.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao preencher a Tabela" + e);

        }

        ModeloTabelas modelo = new ModeloTabelas(dados, Colunas); //Instacia a classe do modelo da Tabela.
        JtableTarifario.setModel(modelo);
        JtableTarifario.getColumnModel().getColumn(0).setPreferredWidth(90); // Tamanho em pixel da coluna
        JtableTarifario.getColumnModel().getColumn(0).setResizable(false);
        JtableTarifario.getColumnModel().getColumn(1).setPreferredWidth(130);
        JtableTarifario.getColumnModel().getColumn(1).setResizable(false);
        JtableTarifario.getColumnModel().getColumn(2).setPreferredWidth(100);
        JtableTarifario.getColumnModel().getColumn(2).setResizable(false);
        JtableTarifario.getColumnModel().getColumn(3).setPreferredWidth(84);
        JtableTarifario.getColumnModel().getColumn(3).setResizable(false);
        JtableTarifario.getTableHeader().setReorderingAllowed(false);
        JtableTarifario.setAutoResizeMode(JtableTarifario.AUTO_RESIZE_OFF);//Não pode ser redimensionada
        JtableTarifario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }

    
